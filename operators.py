#Arithmetic operators
num1=34
num2=10

print(num1+num2)
print(num1-num2)
print(num1*num2)
print(num1/num2)
print(num1%num2) #Modulus operator returns a reminder after a division

#Comparion operators
print(num1<num2)
print(num1>num2)
print(num1<=num2)
print(num1>=num2)
print(num1==num2) #equal to
print(num1!=num2) #not equal to

#Assignment operators
w=10
print(w)

w+=3
print(w)

b=7
b*=2
print(b)

#Logical operators - and, or, not
print(56<100 and 45>23)
print(56<100 or 45>23)
print(not(56<100 or 45>23))


      
